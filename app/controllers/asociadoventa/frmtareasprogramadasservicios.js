function datiempo(num) {
    if(num<0)
    {
        num=num*-1
    }
    d = Math.floor(num / 1440); // 60*24
  h = Math.floor((num - (d * 1440)) / 60);
  m = Math.round(num % 60);

  if (d > 0) {
    return(d + ' dias, ' + h + ' horas, '+ m +' minutos');
  } else {
    return(h + ' horas, '+ m +' minutos');
  }
  }


